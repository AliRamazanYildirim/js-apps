let newList = [1];
newList.push(2);
console.log(newList.push(3)); // 3

//Erklärung: Die Methode push() fügt ein oder mehrere Elemente am Ende eines Arrays hinzu und gibt die neue Länge des Arrays zurück. In diesem Fall wird die Zahl 3 hinzugefügt und die Länge des Arrays beträgt nun 3. Die Methode gibt also 3 zurück.