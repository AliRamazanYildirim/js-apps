console.log(false || null || 'quiz-11.js'); //! quiz-11.js
console.log(false && null && 'quiz-11.js'); //! false

//? Erklärung: Der logische ODER-Operator (||) gibt den ersten Wert zurück, der als true ausgewertet wird. Wenn alle Werte false sind, wird der letzte Wert zurückgegeben.

//? Der logische UND-Operator (&&) gibt den ersten Wert zurück, der als false ausgewertet wird. Wenn alle Werte true sind, wird der letzte Wert zurückgegeben. In diesem Fall ist der erste Wert false, daher wird der letzte Wert 'quiz-11.js' zurückgegeben.
