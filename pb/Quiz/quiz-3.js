const [x,y = 7] = [3];

console.log(x+y); // Output: 10

// In JavaScript können Sie Standardwerte für Variablen festlegen, die nicht definiert sind. In diesem Fall wird y auf 7 gesetzt, wenn der Wert nicht definiert ist. Da y nicht definiert ist, wird y auf 7 gesetzt. Daher wird x + y zu 3 + 7 = 10 ausgewertet.